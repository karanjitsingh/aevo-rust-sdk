//! Generates Rust source for the `aevo-sdk` crate from a [`UnifiedSpec`].
//!
//! Produces two files:
//! * `models.rs`  — type aliases, enums and structs for every schema.
//! * `endpoints.rs` — an `impl AevoClient` block with one async method per
//!   REST operation.

use std::collections::BTreeMap;
use std::fmt::Write as _;

use crate::names::{escape_ident, escape_method_name, to_pascal_case, to_snake_case};
use crate::openapi::{Operation, Schema};
use crate::parser::{EndpointOp, UnifiedSpec};

const HEADER: &str = "// @generated by aevo-codegen. Do not edit by hand.\n\
                      // Run `cargo run -p aevo-codegen` to regenerate.\n\n";

/// The two generated Rust source files.
pub struct GeneratedRest {
    pub models: String,
    pub endpoints: String,
}

/// Generate the REST models and endpoints source.
pub fn generate(spec: &UnifiedSpec) -> GeneratedRest {
    let mut ctx = TypeRegistry::new();

    // First, register top-level named schemas so they map to stable type names.
    for name in spec.schemas.keys() {
        ctx.register_named(name);
    }

    let endpoints = generate_endpoints(spec, &mut ctx);
    // Models must be generated last because endpoint generation creates inline
    // request/response structs that are appended to the model registry.
    let models = generate_models(spec, &mut ctx);

    GeneratedRest { models, endpoints }
}

/// Tracks how schema names map to Rust type names, and accumulates the
/// inline (anonymous) struct/enum definitions discovered while walking schemas.
struct TypeRegistry {
    /// schema name (snake_case) -> Rust type name (PascalCase).
    named: BTreeMap<String, String>,
    /// Generated inline type definitions, keyed by type name for de-dup.
    inline_defs: BTreeMap<String, String>,
    /// Track used type names to avoid collisions.
    used_names: std::collections::BTreeSet<String>,
}

impl TypeRegistry {
    fn new() -> Self {
        Self {
            named: BTreeMap::new(),
            inline_defs: BTreeMap::new(),
            used_names: std::collections::BTreeSet::new(),
        }
    }

    fn register_named(&mut self, schema_name: &str) -> String {
        if let Some(existing) = self.named.get(schema_name) {
            return existing.clone();
        }
        let base = to_pascal_case(schema_name);
        let unique = self.unique_name(&base);
        self.named.insert(schema_name.to_string(), unique.clone());
        unique
    }

    fn unique_name(&mut self, base: &str) -> String {
        let mut candidate = base.to_string();
        let mut i = 2;
        while self.used_names.contains(&candidate) {
            candidate = format!("{base}{i}");
            i += 1;
        }
        self.used_names.insert(candidate.clone());
        candidate
    }

    fn rust_type_for_named(&self, schema_name: &str) -> String {
        self.named
            .get(schema_name)
            .cloned()
            .unwrap_or_else(|| to_pascal_case(schema_name))
    }
}

// ----------------------------------------------------------------------------
// Models
// ----------------------------------------------------------------------------

fn generate_models(spec: &UnifiedSpec, ctx: &mut TypeRegistry) -> String {
    // Resolve all named schemas first (this may register further inline defs).
    let mut named_body = String::new();
    let schema_names: Vec<String> = spec.schemas.keys().cloned().collect();
    for name in &schema_names {
        let schema = spec.schemas.get(name).unwrap().clone();
        let type_name = ctx.rust_type_for_named(name);
        emit_named_schema(&mut named_body, &type_name, &schema, ctx);
    }

    let mut out = String::new();
    out.push_str(HEADER);
    out.push_str(
        "#![allow(clippy::all)]\n\
         use serde::{Deserialize, Serialize};\n\n",
    );
    out.push_str(&named_body);

    // Inline structs/enums collected during model + endpoint generation.
    for def in ctx.inline_defs.values() {
        out.push_str(def);
        out.push('\n');
    }

    out
}

/// Emit a top-level named schema as a type alias, enum, or struct.
fn emit_named_schema(out: &mut String, type_name: &str, schema: &Schema, ctx: &mut TypeRegistry) {
    if let Some(doc) = &schema.description {
        emit_doc(out, doc, "");
    }

    // String enum.
    if !schema.enum_values.is_empty() && schema.ty.as_deref() == Some("string") {
        emit_string_enum(out, type_name, schema);
        return;
    }

    // Object with properties -> struct.
    if schema.ty.as_deref() == Some("object") && !schema.properties.is_empty() {
        emit_struct(out, type_name, schema, ctx);
        return;
    }

    // Everything else is a type alias to a primitive / Value / Vec.
    let inner = rust_type_alias_target(schema, ctx);
    let _ = writeln!(out, "pub type {type_name} = {inner};\n");
}

/// Resolve the aliased target type for a non-object, non-enum schema.
fn rust_type_alias_target(schema: &Schema, ctx: &mut TypeRegistry) -> String {
    match schema.ty.as_deref() {
        Some("string") => "String".to_string(),
        Some("integer") => "i64".to_string(),
        Some("number") => "f64".to_string(),
        Some("boolean") => "bool".to_string(),
        Some("array") => {
            let inner = schema
                .items
                .as_ref()
                .map(|it| register_schema_type(it, ctx, "Item"))
                .unwrap_or_else(|| "serde_json::Value".to_string());
            format!("Vec<{inner}>")
        }
        _ => "serde_json::Value".to_string(),
    }
}

/// Emit a string enum type.
fn emit_string_enum(out: &mut String, type_name: &str, schema: &Schema) {
    out.push_str("#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]\n");
    let _ = writeln!(out, "pub enum {type_name} {{");
    let mut seen = std::collections::BTreeSet::new();
    for value in &schema.enum_values {
        let Some(v) = value.as_str() else { continue };
        let mut variant = to_pascal_case(v);
        if variant.is_empty() {
            variant = "Empty".to_string();
        }
        // Avoid duplicate variant identifiers.
        let mut unique = variant.clone();
        let mut i = 2;
        while !seen.insert(unique.clone()) {
            unique = format!("{variant}{i}");
            i += 1;
        }
        let _ = writeln!(out, "    #[serde(rename = \"{v}\")]");
        let _ = writeln!(out, "    {unique},");
    }
    out.push_str("}\n\n");
}

/// Emit a struct for an object schema. Property field types resolve to named
/// types (for `$ref`) or inline types (registered into `ctx.inline_defs`).
fn emit_struct(out: &mut String, type_name: &str, schema: &Schema, ctx: &mut TypeRegistry) {
    out.push_str("#[derive(Debug, Clone, Serialize, Deserialize)]\n");
    let _ = writeln!(out, "pub struct {type_name} {{");
    for (prop_name, prop_schema) in &schema.properties {
        let required = schema.required.iter().any(|r| r == prop_name);
        emit_field(out, type_name, prop_name, prop_schema, required, ctx);
    }
    out.push_str("}\n\n");
}

/// Emit a single struct field.
fn emit_field(
    out: &mut String,
    parent: &str,
    prop_name: &str,
    prop_schema: &Schema,
    required: bool,
    ctx: &mut TypeRegistry,
) {
    if let Some(doc) = &prop_schema.description {
        emit_doc(out, doc, "    ");
    }
    let field_ident = escape_ident(&to_snake_case(prop_name));
    let nested_hint = format!("{parent}{}", to_pascal_case(prop_name));
    let base_type = register_schema_type(prop_schema, ctx, &nested_hint);
    let ty = if required {
        base_type
    } else {
        format!("Option<{base_type}>")
    };

    let rust_bare = field_ident.trim_start_matches("r#");
    if prop_name != rust_bare {
        let _ = writeln!(out, "    #[serde(rename = \"{prop_name}\")]");
    }
    if !required {
        out.push_str("    #[serde(skip_serializing_if = \"Option::is_none\", default)]\n");
    }
    let _ = writeln!(out, "    pub {field_ident}: {ty},");
}

