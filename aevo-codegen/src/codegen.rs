/// Emit a `///` doc comment, escaping newlines and trimming noise.
fn emit_doc(out: &mut String, text: &str, indent: &str) {
    let cleaned = text.replace('\r', " ").replace('\n', " ");
    let cleaned = cleaned.trim();
    if cleaned.is_empty() {
        return;
    }
    // Wrap long docs to a single line each (keep it simple & deterministic).
    for line in textwrap_simple(cleaned, 100) {
        let _ = writeln!(out, "{indent}/// {line}");
    }
}

/// Extremely small word-wrap that never panics and is deterministic.
fn textwrap_simple(text: &str, width: usize) -> Vec<String> {
    let mut lines = Vec::new();
    let mut current = String::new();
    for word in text.split_whitespace() {
        if current.is_empty() {
            current.push_str(word);
        } else if current.len() + 1 + word.len() > width {
            lines.push(std::mem::take(&mut current));
            current.push_str(word);
        } else {
            current.push(' ');
            current.push_str(word);
        }
    }
    if !current.is_empty() {
        lines.push(current);
    }
    lines
}
